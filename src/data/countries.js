/** @returns {{ code: string, name: string }[]} */
export function getCountriesForProfileForm() {
  const byCode = new Map(RAW.map(([code, name]) => [code, { code, name }]));
  const pri = [];
  const seen = new Set();
  for (const code of PRIORITY_COUNTRY_CODES) {
    const row = byCode.get(code);
    if (row) {
      pri.push(row);
      seen.add(code);
    }
  }
  const rest = RAW.map(([code, name]) => ({ code, name }))
    .filter((r) => !seen.has(r.code))
    .sort((a, b) => a.name.localeCompare(b.name, "en"));
  return [...pri, ...rest];
}

/** @param {string | null | undefined} code */
export function countryNameByCode(code) {
  if (typeof code !== "string" || code.length !== 2) return "";
  const u = code.toUpperCase();
  const row = RAW.find(([c]) => c === u);
  return row ? row[1] : "";
}
